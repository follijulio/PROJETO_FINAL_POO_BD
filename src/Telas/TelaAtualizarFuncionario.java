      PreparedStatement sttmt = conexao.prepareStatement(
        "SELECT * FROM funcionario;"
      );
      ResultSet rst = sttmt.executeQuery();
      FuncionarioDAO autor = new FuncionarioDAO();
      while (rst.next()) {
        String nome = rst.getString("nome");
        String codigo = rst.getString("codigo");

        DefaultTableModel modelo = (DefaultTableModel) tabela_funcionarios.getModel();
        modelo.addRow(new Object[] { codigo, nome });
      }
