  private void continuarActionPerformed(java.awt.event.ActionEvent evt) { //GEN-FIRST:event_continuarActionPerformed
    // TODO add your handling code here:
    FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
    int id = funcionarioDAO.procurarFuncionarioCodigo(area_codigo.getText());
    Funcionario funcionario;
    funcionario = funcionarioDAO.pegarFuncionario(id);

    if (!vmanter_codigo) {
      funcionario.setCodigo(area_codigo_novo.getText());
    }
    if (!vmanter_nome) {
      funcionario.setNome(area_nome_novo.getText());
    }
    if (!vmanter_salario) {
      funcionario.setSalario(Double.parseDouble(area_salario_novo.getText()));
    }
    if (!vmanter_senha) {
      funcionario.setSenha(area_senha_nova.getText());
    }
    boolean situacao = funcionarioDAO.atualizarFuncionario(funcionario, id);
    if (situacao) {
      TelaAtualizarFuncionario tela = new TelaAtualizarFuncionario();
      tela.setVisible(true);
    } else {
      JOptionPane.showMessageDialog(
        null,
        "ALGUM ERRO ENCONTRADO",
        "ALERTA",
        JOptionPane.ERROR_MESSAGE
      );
    }
  } //GEN-LAST:event_continuarActionPerformed

